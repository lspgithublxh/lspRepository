package com.bj58.fang.hugopenapi.client.exception;

public class ZFException extends Exception{

	private static final long serialVersionUID = 1L;

	public ZFException() {
		super();
	}

	public ZFException(String message, Throwable cause) {
		super(message, cause);
	}

	public ZFException(String message) {
		super(message);
	}

	public ZFException(Throwable cause) {
		super(cause);
	}

	
}
