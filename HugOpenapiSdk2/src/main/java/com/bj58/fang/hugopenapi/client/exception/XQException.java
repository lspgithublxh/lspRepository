package com.bj58.fang.hugopenapi.client.exception;

public class XQException extends Exception{

	private static final long serialVersionUID = 1L;

	public XQException() {
		super();
	}

	public XQException(String message, Throwable cause) {
		super(message, cause);
	}

	public XQException(String message) {
		super(message);
	}

	public XQException(Throwable cause) {
		super(cause);
	}

	
}
