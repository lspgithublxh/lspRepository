package com.bj58.fang.hugopenapi.client.exception;

public class ESFException extends Exception{

	private static final long serialVersionUID = 1L;

	public ESFException() {
		super();
	}

	public ESFException(String message, Throwable cause) {
		super(message, cause);
	}

	public ESFException(String message) {
		super(message);
	}

	public ESFException(Throwable cause) {
		super(cause);
	}

	
}
